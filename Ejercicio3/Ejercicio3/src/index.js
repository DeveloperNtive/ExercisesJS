const maximoNumeroHoras = equipo => {
  // N -> Numero de tareas asignadas para el equipo j
  // T -> Numero de horas promedio de las tareas en el equipo j
  // R -> La duracion en horas de la tarea mas pequeña en el equipo j

  //resultado
  const intArray = []

  //Solucion
  for (let index = 0; index < equipo.length; index++) {
    const element = equipo[index]
    var n = element[0], //Numero de tareas
      t = element[1], //Tiempo promedio de todas las tareas
      r = element[2], //Tarea mas pequeña
      to = 0 //Suma tiempo de otras tareas
    if (t != 1) {
      if (n >= 3) {
        to = t * (n - 1)
        intArray.push(to)
      } else {
        to = t * n - r
        intArray.push(to)
      }
    } else {
      intArray.push(1)
    }
  }
  //retorno
  return intArray
}

